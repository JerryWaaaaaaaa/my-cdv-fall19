function processData(data){

    // loop through the data and
    // divide them into days -> locations
    // get item and give a color comb to it
    let colorI = 1;

    for (let i = 0; i < data.length; i ++ ){
        // get datum for each item
        let datum = data[i];
        let dayIndex = getDayIndex(datum);
        let locIndex = getLocIndex(datum);
        // let timeIndex = getTimeIndex(datum);
        days[dayIndex][locIndex].push(datum);

        if (!(datum["purpose"] in purposeColors)) {
            purposeColors[datum.purpose] = colors[colorI];
            colorI += 1;
        }
    }

    // process the color Array
    purposeKeys = Object.keys(purposeColors);
    purposeKeys.forEach(function(d, i){ keyMatchingColors[i] = purposeColors[d]; });
    colorItemWidth = ( canvasWidth/2 - paddingY * 2 ) / purposeKeys.length;

    // sort the items based on time
    for (let i = 0; i < days.length; i ++ ){
        for (let j = 0; j < days[i].length - 1; j ++  ){
            d = days[i][j];
            // more than one item in a location
            if (d.length > 1) {
                let endIndex = d.length - 1;
                for (let k = 0; k < d.length - 1; k ++ ) {
                    for(let l = 0; l < endIndex; l ++){
                        t = parseInt(d[l].time.split(":")[0]);
                        nextT = parseInt(d[l+1]["time"].split(":")[0]);
                        if ( t > nextT ){
                            temp = d[l+1];
                            d[l+1] = d[l];
                            d[l] = temp;
                        }
                    }
                    endIndex -= 1;
                    if (endIndex < 1){
                      break;
                    }
                }
            }
        }
    }

    console.log(purposeColors);

}

